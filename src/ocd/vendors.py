# ── Amazon Q ────────────────────────────────────────────────────────────────


def materialize_amazonq(
    db: sqlite3.Connection, target: Path, force: bool = False
) -> dict[str, int]:
    """Materialize OCD configuration for Amazon Q Developer.

    Creates .amazonq/rules/*.md files with Purpose, Instructions,
    and Priority sections.
    """
    target.mkdir(parents=True, exist_ok=True)
    count = 0
    rows = db.execute("SELECT name, description, body FROM rules ORDER BY name").fetchall()
    for name, description, body in rows:
        path = target / f"{name}.md"
        if path.exists() and not force:
            continue
        lines = [
            f"# {name.replace('-', ' ').title()}",
            "",
            f"**Purpose:** {description}",
            "",
            "**Instructions:**",
            "",
        ]
        for line in body.strip().splitlines():
            lines.append(f"{line}")
        lines.append("")
        lines.append("**Priority:** high")
        lines.append("")
        path.write_text("\n".join(lines))
        count += 1
    return {"rules": count}


# ── AGENTS.md (cross-vendor) ────────────────────────────────────────────────


def generate_agents_md(db: sqlite3.Connection) -> str:
    """Generate a project-root AGENTS.md file.

    Produces a condensed, vendor-neutral instruction file that any
    LLM tool can read. Not a copy of .claude/ content — a synthesis.
    """
    lines = ["# O.C.D. Project Agents", ""]

    # Standards reference (version + hash only, not full text)
    row = db.execute("SELECT version, hash FROM standards WHERE id = 1").fetchone()
    if row:
        version, hash_val = row
        lines.append(f"Standards reference: v{version} [{hash_val}]")
        lines.append("")

    # Agent manifest
    lines.append("## Agents")
    lines.append("")
    agents = db.execute("SELECT name, frontmatter, body FROM agents ORDER BY name").fetchall()
    for name, _frontmatter, body in agents:
        first_line = body.strip().split("\n")[0] if body.strip() else ""
        lines.append(f"- **{name}**: {first_line}")
    lines.append("")

    # Key rules (condensed)
    lines.append("## Key Rules")
    lines.append("")
    rules = db.execute("SELECT name, description FROM rules ORDER BY name").fetchall()
    for name, description in rules:
        lines.append(f"- **{name}**: {description}")
    lines.append("")

    # Skills summary
    lines.append("## Skills")
    lines.append("")
    skills = db.execute("SELECT name, description FROM skills ORDER BY name").fetchall()
    for name, description in skills:
        lines.append(f"- **{name}**: {description}")
    lines.append("")

    return "\n".join(lines) + "\n"


