def _yaml_quote(value: str) -> str:
    """Quote a YAML value if it contains special characters."""
    if any(
        c in value
        for c in (
            ":",
            "#",
            "{",
            "}",
            "[",
            "]",
            ",",
            "&",
            "*",
            "?",
            "|",
            "-",
            "<",
            ">",
            "=",
            "!",
            "%",
            "@",
            "`",
        )
    ):
        return f'"{value}"'
    return value
